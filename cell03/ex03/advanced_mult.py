# ใช้ while loop เพื่อแสดงตารางสูตรคูณตั้งแต่ 0 ถึง 10

i = 0
while i <= 10:  # ลูปสำหรับแต่ละตาราง
    # แสดง "Table de i:"
    print(f"Table de {i}: ", end="")

    # ลูปคำนวณค่าภายในตารางสูตรคูณ
    j = 0
    while j <= 10:  # คูณตัวเลขตั้งแต่ 0 ถึง 10
        print(f"{i * j:2}", end=" ")  # ใช้ :2 เพื่อจัดให้ตัวเลขชิดซ้าย และเพิ่มช่องว่าง
        j += 1

    # แสดงบรรทัดใหม่เมื่อจบตารางหนึ่ง
    print()
    i += 1